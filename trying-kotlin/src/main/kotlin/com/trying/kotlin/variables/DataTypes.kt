package com.trying.kotlin.variables

fun main() {
    // String data type
    val stringType = "Hello World"

    // Integer data type
    val integerType = 20

    // Boolean data type
    val booleanType = false

    // Double data type
    val doubleType = 2.0

    // Float data type
    val floatType = 2.0f

    // Character data type
    val characterType = 'C'

    println("The value of type string is: $stringType.")
    println("The value of type integer is: $integerType.")
    println("The value of type boolean is: $booleanType.")
    println("The value of type double is: $doubleType.")
    println("The value of type float is: $floatType.")
    println("The value of type character is: $characterType.")
}