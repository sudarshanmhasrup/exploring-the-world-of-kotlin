package variables

fun main() {

    // String data type
    val stringType = "Hey! This is a string"

    // Integer data type
    val integerType = 40

    // Boolean data type
    val booleanType = false

    // Char data type
    val characterType = 'A'

    // Double data type
    val doubleType = 70.50

    // Float data type
    val floatType = 45.0f

    println("The value of String data type is: $stringType.")
    println("The value of Int data type is: $integerType.")
    println("The value of Boolean data type is: $booleanType.")
    println("The value of Char data type is: $characterType.")
    println("The value of Double data type is: $doubleType.")
    println("The value of Float data type is: $floatType.")
}